package com.kh.picachubaedal.logic;

public class Location {

	private String x; // 위도
    private String y; // 경도

    // 생성자, getter, setter 등 필요한 메서드를 추가
    public Location() {
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
